#include "push_swap.h"

void	min_max(long long *ptr, int size)
{
	int	i;

	i = 0;
	while (i < size)
	{
		if (ptr[i] < -2147483648)
		{
			free(ptr);
			werror(1);
		}
		if (ptr[i] > 2147483647)
		{
			free(ptr);
			werror(1);
		}
		i++;
	}
}
